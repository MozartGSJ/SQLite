public class LocalVo {

    private int id_local;
    private String desc_local;
    private String km_ini_local;
    private String km_fim_local;

    public int getId_local() {
        return id_local;
    }

    public void setId_local(int id_local) {
        this.id_local = id_local;
    }

    public String getDesc_local() {
        return desc_local;
    }

    public void setDesc_local(String desc_local) {
        this.desc_local = desc_local;
    }

    public String getKm_ini_local() {
        return km_ini_local;
    }

    public void setKm_ini_local(String km_ini_local) {
        this.km_ini_local = km_ini_local;
    }

    public String getKm_fim_local() {
        return km_fim_local;
    }

    public void setKm_fim_local(String km_fim_local) {
        this.km_fim_local = km_fim_local;
    }
}
